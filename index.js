function showIdle() {
  clearInterval(talkingInterval);
  face.textContent = idleFace;
}

function startTalking() {
  let toggle = false;
  clearInterval(talkingInterval);
  talkingInterval = setInterval(() => {
    face.textContent = toggle ? idleFace : talkingFace;
    toggle = !toggle;
  }, 300);
}

function showGlitch(duration = 1200) {
  clearInterval(talkingInterval);
  face.textContent = glitchFace;
  setTimeout(showIdle, duration);
}

function typeText(text, speed = 40, callback) {
  let i = 0;
  statusLine.textContent = "";
  startTalking();
  const interval = setInterval(() => {
    statusLine.textContent += text[i];
    i++;
    if (i === text.length) {
      clearInterval(interval);
      showIdle();
      if (callback) callback();
    }
  }, speed);
}


